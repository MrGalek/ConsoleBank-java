package com.github.mrgalek;

public class Person
{
    private int number;
    private String name;
    private boolean type;


    public int getNumber()
    {
        return number;
    }

    public void setNumber(int number)
    {
        this.number = number;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public void setType(boolean type)
    {
        this.type = type;
    }


}
